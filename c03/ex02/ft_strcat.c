char	*ft_strcat(char *dest, char *src)
{
	int	n;
	int	m;

	n = 0;
	while (dest[n] != '\0')
		n++;
	m = 0;
	while (src[m] != '\0')
	{
		dest[n + m] = src[m];
		m++;
	}
	dest[n + m] = src[m];
	return (dest);
}
